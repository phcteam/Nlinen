    <script>
        const app = Vue.createApp({
            data() {
                return {
                    loading: false,
                    pageContent: `{!! addslashes($content ?? '') !!}`,
                    activePage: '', // ตัวแปรเก็บชื่อหน้าเพื่อใช้ในการแสดงเมนู active
                };
            },
            mounted() {
                // ตรวจสอบ path ปัจจุบันเพื่อเซ็ตค่า activePage
                const currentPath = window.location.pathname.replace('/general/', '') || 'home';
                this.activePage = currentPath; // ตั้งค่า activePage เป็นหน้าที่เลือกใน URL

                if (!this.pageContent) {
                    this.loadPage(currentPath, false);
                }

                window.addEventListener('popstate', (event) => {
                    if (event.state && event.state.page) {
                        this.loadPage(event.state.page, false);
                    }
                });
            },
            methods: {
                // ฟังก์ชันสำหรับการโหลดหน้าใหม่
                async loadPage(page, pushState = true) {
                    this.loading = true;
                    try {
                        const response = await fetch(`/general/${page}`, {
                            headers: {
                                'X-Requested-With': 'XMLHttpRequest'
                            }
                        });
                        if (!response.ok) throw new Error("ไม่สามารถโหลดเนื้อหาได้");

                        const html = await response.text();
                        this.pageContent = html;

                        if (pushState) {
                            history.pushState({
                                page
                            }, "", `/general/${page}`);
                        }
                        this.activePage = page; // อัพเดทค่า activePage เมื่อโหลดหน้าใหม่
                    } catch (error) {
                        this.pageContent = `<h3 class="text-danger">❌ ไม่สามารถโหลดเนื้อหาได้</h3>`;
                        console.error("Error loading page:", error);
                    } finally {
                        this.loading = false;
                    }
                },
                // ฟังก์ชันตั้งค่า activePage เมื่อคลิกที่เมนู
                setActivePage(page) {
                    this.activePage = page;
                }
            }
        });

        app.mount('#app');
    </script>


    <script>
        (function($) {
            "use strict"; // Start of use strict

            $("#sidebarToggle, #sidebarToggleTop").on('click', function(e) {
                $("body").toggleClass("sidebar-toggled");
                $(".sidebar").toggleClass("toggled");
                if ($(".sidebar").hasClass("toggled")) {
                    $('.sidebar .collapse').collapse('hide');
                };
            });

            $(window).resize(function() {
                if ($(window).width() < 768) {
                    $('.sidebar .collapse').collapse('hide');
                };

                if ($(window).width() < 480 && !$(".sidebar").hasClass("toggled")) {
                    $("body").addClass("sidebar-toggled");
                    $(".sidebar").addClass("toggled");
                    $('.sidebar .collapse').collapse('hide');
                };
            });

            $('body.fixed-nav .sidebar').on('mousewheel DOMMouseScroll wheel', function(e) {
                if ($(window).width() > 768) {
                    var e0 = e.originalEvent,
                        delta = e0.wheelDelta || -e0.detail;
                    this.scrollTop += (delta < 0 ? 1 : -1) * 30;
                    e.preventDefault();
                }
            });

            // Scroll to top button appear
            $(document).on('scroll', function() {
                var scrollDistance = $(this).scrollTop();
                if (scrollDistance > 100) {
                    $('.scroll-to-top').fadeIn();
                } else {
                    $('.scroll-to-top').fadeOut();
                }
            });

            // Smooth scrolling using jQuery easing
            $(document).on('click', 'a.scroll-to-top', function(e) {
                var $anchor = $(this);
                $('html, body').stop().animate({
                    scrollTop: ($($anchor.attr('href')).offset().top)
                }, 1000, 'easeInOutExpo');
                e.preventDefault();
            });

        })(jQuery); // End of use strict
